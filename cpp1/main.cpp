#include <iostream>

int main() {
  int x, y;
  std::cout << "x:";
  std::cin >> x;
  std::cout << "y:";
  std::cin >> y;
  std::cout << "x = " << x << std::endl;
  std::cout << "y = " << y << std::endl;
  std::cout << "(x + y) / 2 = " << (x + y) / 2 << std::endl;
  std::cout << "x + y = " << (x + y) << std::endl;
}
